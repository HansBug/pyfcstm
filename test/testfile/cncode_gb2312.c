#include <stdio.h>
#include <stdlib.h>
#include <string.h>

// 定义一些常量
#define 最大值 100
#define 缓冲区大小 256

// 结构体定义
typedef struct 学生信息 {
    int 学号;
    char 姓名[50];
    float 成绩;
} 学生;

/*
 * 函数: 计算平均分
 * 参数: 成绩数组和数量
 * 返回: 平均分数值
 * 说明: 这里包含了一些中文字符用于测试各种编码
 */
float 计算平均分(float 分数[], int 数量) {
    float 总分 = 0.0;
    int i;

    // 遍历所有分数并累加
    for (i = 0; i < 数量; i++) {
        总分 += 分数[i];  // 累加每个学生的分数
    }

    // 返回平均值，注意处理除零情况
    return 数量 > 0 ? 总分 / 数量 : 0;
}

/**
 * 主函数 - 程序入口点
 * 包含多种中文字符和符号：【】、；：''""，。？！（）
 */
int main() {
    // 声明变量
    int i, 学生数量 = 3;
    float 所有分数[最大值];
    char 临时缓冲区[缓冲区大小];
    学生 班级[10];  // 班级学生数组

    printf("这是一个测试程序，包含中文注释和变量名\n");

    /* 初始化学生信息 */
    班级[0].学号 = 1001;
    strcpy(班级[0].姓名, "张三");  // 复制姓名
    班级[0].成绩 = 85.5;

    班级[1].学号 = 1002;
    strcpy(班级[1].姓名, "李四");  // 另一个学生
    班级[1].成绩 = 92.0;

    班级[2].学号 = 1003;
    strcpy(班级[2].姓名, "王五");  // 第三个学生
    班级[2].成绩 = 78.5;

    // 收集所有分数用于计算
    for (i = 0; i < 学生数量; i++) {
        所有分数[i] = 班级[i].成绩;

        // 输出每个学生的信息
        printf("学号：%d，姓名：%s，成绩：%.1f\n",
               班级[i].学号, 班级[i].姓名, 班级[i].成绩);
    }

    // 计算并显示平均分
    float 平均分 = 计算平均分(所有分数, 学生数量);
    printf("全班平均分是：%.2f\n", 平均分);

    /*
     * 以下是一些中文标点符号测试：
     * 句号。问号？感叹号！逗号，顿号、冒号：分号；
     * 引号""''括号（）《》【】｛｝
     */

    return 0;  // 正常退出
}